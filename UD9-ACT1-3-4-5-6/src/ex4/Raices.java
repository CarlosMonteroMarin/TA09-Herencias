package ex4;

public class Raices {
	//Atributos
	private int a;
	private int b;
	private int c;
	
	//Constructor por defecto
	public Raices() {
		
	}
	
	//Constructor con los tres parametros
	public Raices(int a, int b, int c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	//Getters y setters
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getC() {
		return c;
	}

	public void setC(int c) {
		this.c = c;
	}

	
	
	
	
	
	
	
	
	

}
